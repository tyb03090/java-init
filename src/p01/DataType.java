package p01;

public class DataType {

	public static void main(String[] args) {
		char c = '1';				// 간단한 문자형 'char'
		int i = 1;					// 정수형 'int'
		
		short s = (short)i;			// 2byte 크기의 문자형 'short'
		
		long l = 1000000000L;		// 8byte 크기의 문자형 'long'
		
		float f = 1.1F;				// 실수형 'float'
		double d = 1.1;				// float 보다 큰 실수형 'double'
		boolean bl = true;			// 논리형 'boolean'
	}

}
